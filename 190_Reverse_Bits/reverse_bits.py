class Solution:
    # @param n, an integer
    # @return an integer
    def reverseBits(self, n):
        MSK_16_F = 0b11111111111111110000000000000000
        MSK_16_B = 0b00000000000000001111111111111111
        MSK_08_F = 0b11111111000000001111111100000000
        MSK_08_B = 0b00000000111111110000000011111111
        MSK_04_F = 0b11110000111100001111000011110000
        MSK_04_B = 0b00001111000011110000111100001111
        MSK_02_F = 0b11001100110011001100110011001100
        MSK_02_B = 0b00110011001100110011001100110011
        MSK_01_F = 0b10101010101010101010101010101010
        MSK_01_B = 0b01010101010101010101010101010101
        n = ( n & MSK_16_F ) >> 16 | ( n & MSK_16_B ) << 16
        n = ( n & MSK_08_F ) >> 8  | ( n & MSK_08_B ) << 8
        n = ( n & MSK_04_F ) >> 4  | ( n & MSK_04_B ) << 4
        n = ( n & MSK_02_F ) >> 2  | ( n & MSK_02_B ) << 2
        n = ( n & MSK_01_F ) >> 1  | ( n & MSK_01_B ) << 1
        return n
        
